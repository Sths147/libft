#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s1;

	i = -1;
	s1 = (char *)s;
	while (s1[i] != '\0')
	{
		i++;
		if (s1[i] == c)
			return (&s1[i]);
	}
	return (NULL);
}
